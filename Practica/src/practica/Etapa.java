package practica;

public class Etapa {

	private Material m1;
	private Material m2;

	public Etapa(Material m1, Material m2) {
		super();
		this.m1 = m1;
		this.m2 = m2;
	}

	public Material getM1() {
		return m1;
	}

	public void setM1(Material m1) {
		this.m1 = m1;
	}

	public Material getM2() {
		return m2;
	}

	public void setM2(Material m2) {
		this.m2 = m2;
	}
	
}
